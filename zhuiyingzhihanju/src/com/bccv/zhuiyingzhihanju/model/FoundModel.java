package com.bccv.zhuiyingzhihanju.model;

import java.util.List;

public class FoundModel {

	
	private List<Movie> slide;
	
	private List<Movie> news;
	private List<Movie> tv;
	
	MovieNews variety;
	
	
	MovieNews movie;
	
	
	private List<Movie> music;


	public List<Movie> getSlide() {
		return slide;
	}


	public void setSlide(List<Movie> slide) {
		this.slide = slide;
	}


	public List<Movie> getNews() {
		return news;
	}


	public void setNews(List<Movie> news) {
		this.news = news;
	}


	public List<Movie> getTv() {
		return tv;
	}


	public void setTv(List<Movie> tv) {
		this.tv = tv;
	}


	public List<Movie> getMusic() {
		return music;
	}


	public void setMusic(List<Movie> music) {
		this.music = music;
	}


	public MovieNews getVariety() {
		return variety;
	}


	public void setVariety(MovieNews variety) {
		this.variety = variety;
	}


	public MovieNews getMovie() {
		return movie;
	}


	public void setMovie(MovieNews movie) {
		this.movie = movie;
	}


	@Override
	public String toString() {
		return "FoundModel [slide=" + slide + ", news=" + news + ", tv=" + tv + ", variety=" + variety + ", movie="
				+ movie + ", music=" + music + "]";
	}
	
	
	
	
	
	
	
}
