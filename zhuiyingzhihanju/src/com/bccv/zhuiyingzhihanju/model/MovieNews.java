public class MovieNews {

	
	private List<Movie> big;
	private List<Movie>list;
	public List<Movie> getBig() {
		return big;
	}
	public void setBig(List<Movie> big) {
		this.big = big;
	}
	public List<Movie> getList() {
		return list;
	}
	public void setList(List<Movie> list) {
		this.list = list;
	}
	@Override
	public String toString() {
		return "MovieNews [big=" + big + ", list=" + list + "]";
	}
	
	
	
	
	
	
}
