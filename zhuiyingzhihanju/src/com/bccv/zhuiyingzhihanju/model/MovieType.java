package com.bccv.zhuiyingzhihanju.model;

import java.util.List;

public class MovieType {
	private List<MovieArea> area;
	private List<MovieYear> year;
	private List<MovieGenres> type;

	public List<MovieArea> getArea() {
		return area;
	}

	public void setArea(List<MovieArea> area) {
		this.area = area;
	}

	public List<MovieYear> getYear() {
		return year;
	}

	public void setYear(List<MovieYear> year) {
		this.year = year;
	}

	public List<MovieGenres> getType() {
		return type;
	}

	public void setType(List<MovieGenres> type) {
		this.type = type;
	}

}
