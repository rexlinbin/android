package com.bccv.tianji.model;

public class GameDownloadInfo {
private String download_command;
private String download_status;
private String start_download_time;
private float download_size;
private String game_id;
private String user_id;
private String id;
public String getDownload_command() {
	return download_command;
}
public void setDownload_command(String download_command) {
	this.download_command = download_command;
}
public String getDownload_status() {
	return download_status;
}
public void setDownload_status(String download_status) {
	this.download_status = download_status;
}
public String getStart_download_time() {
	return start_download_time;
}
public void setStart_download_time(String start_download_time) {
	this.start_download_time = start_download_time;
}
public float getDownload_size() {
	return download_size;
}
public void setDownload_size(float download_size) {
	this.download_size = download_size;
}
public String getGame_id() {
	return game_id;
}
public void setGame_id(String game_id) {
	this.game_id = game_id;
}
public String getUser_id() {
	return user_id;
}
public void setUser_id(String user_id) {
	this.user_id = user_id;
}
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}

}
